"""
LLM-Driven Experiment Guide Generator

Replaces the template-based guide generator with dynamic, innovation-specific
guides generated by LLM. Produces foolproof step-by-step instructions.

Usage:
    from app.experiment import LLMGuideGenerator

    generator = LLMGuideGenerator()
    guide = await generator.generate_guide(
        innovation_data={...},
        feasibility_result={...},
        target_venue="NeurIPS"
    )
"""

from __future__ import annotations

import json
import logging
from dataclasses import dataclass, field
from typing import Any, Dict, List, Optional

from app.paper_writing.engine import LLMClient

logger = logging.getLogger(__name__)


_GUIDE_SYSTEM_PROMPT = """You are an expert technical writer who creates foolproof experiment guides.

Your guides must be so clear that a complete beginner could follow them successfully.

Each step must:
1. Have a clear objective ("What you will accomplish in this step")
2. Provide exact commands or actions (copy-paste ready)
3. Show expected output (so the user knows they're on track)
4. Include a troubleshooting note for common mistakes
5. Take less than 15 minutes to complete

Output MUST be valid JSON with this exact structure:
{
  "title": "Experiment title",
  "description": "One-sentence summary",
  "difficulty": "beginner" | "intermediate" | "advanced",
  "estimated_time": "e.g. 2-3 hours",
  "prerequisites": ["Python 3.9+", "GPU with 8GB+ VRAM", ...],
  "safety_notes": ["..."],
  "steps": [
    {
      "number": 1,
      "title": "Step title",
      "description": "Detailed instructions",
      "commands": ["exact command 1", "exact command 2"],
      "expected_output": "What the user should see",
      "tips": ["Helpful tip"],
      "warnings": ["Warning about common mistake"],
      "checklist": ["Check item 1", "Check item 2"],
      "estimated_time": "10 minutes"
    }
  ],
  "troubleshooting": [
    {
      "problem": "Common problem",
      "symptom": "How to recognize it",
      "cause": "Why it happens",
      "solution": "Step-by-step fix"
    }
  ],
  "resources": {
    "hardware": ["..."],
    "software": ["..."],
    "datasets": ["..."],
    "libraries": ["..."]
  },
  "expected_results": "What a successful experiment looks like",
  "data_collection_template": {
    "metrics_to_record": ["accuracy", "loss", ...],
    "figures_to_generate": ["training_curve", "confusion_matrix", ...],
    "files_to_save": ["results.json", "model.pt", ...]
  }
}
"""


@dataclass
class GuideStep:
    number: int
    title: str
    description: str
    commands: List[str] = field(default_factory=list)
    expected_output: str = ""
    tips: List[str] = field(default_factory=list)
    warnings: List[str] = field(default_factory=list)
    checklist: List[str] = field(default_factory=list)
    estimated_time: str = ""

    def to_dict(self) -> Dict[str, Any]:
        return {
            "number": self.number,
            "title": self.title,
            "description": self.description,
            "commands": self.commands,
            "expected_output": self.expected_output,
            "tips": self.tips,
            "warnings": self.warnings,
            "checklist": self.checklist,
            "estimated_time": self.estimated_time,
        }


@dataclass
class TroubleshootingItem:
    problem: str
    symptom: str
    cause: str
    solution: str

    def to_dict(self) -> Dict[str, Any]:
        return {
            "problem": self.problem,
            "symptom": self.symptom,
            "cause": self.cause,
            "solution": self.solution,
        }


@dataclass
class ExperimentGuide:
    """A complete experiment guide"""
    title: str
    description: str
    difficulty: str = "intermediate"
    estimated_time: str = ""
    prerequisites: List[str] = field(default_factory=list)
    safety_notes: List[str] = field(default_factory=list)
    steps: List[GuideStep] = field(default_factory=list)
    troubleshooting: List[TroubleshootingItem] = field(default_factory=list)
    resources: Dict[str, List[str]] = field(default_factory=dict)
    expected_results: str = ""
    data_collection_template: Dict[str, Any] = field(default_factory=dict)

    def to_dict(self) -> Dict[str, Any]:
        return {
            "title": self.title,
            "description": self.description,
            "difficulty": self.difficulty,
            "estimated_time": self.estimated_time,
            "prerequisites": self.prerequisites,
            "safety_notes": self.safety_notes,
            "steps": [s.to_dict() for s in self.steps],
            "troubleshooting": [t.to_dict() for t in self.troubleshooting],
            "resources": self.resources,
            "expected_results": self.expected_results,
            "data_collection_template": self.data_collection_template,
        }

    def to_markdown(self) -> str:
        """Convert guide to Markdown for download/printing"""
        lines = [
            f"# {self.title}",
            "",
            f"**Difficulty**: {self.difficulty}",
            f"**Estimated Time**: {self.estimated_time}",
            "",
            f"## Description",
            f"{self.description}",
            "",
        ]

        if self.prerequisites:
            lines.extend(["## Prerequisites", ""])
            for p in self.prerequisites:
                lines.append(f"- {p}")
            lines.append("")

        if self.safety_notes:
            lines.extend(["## Safety Notes", ""])
            for note in self.safety_notes:
                lines.append(f"- ⚠️ {note}")
            lines.append("")

        if self.resources:
            lines.extend(["## Resources", ""])
            for category, items in self.resources.items():
                if items:
                    lines.append(f"### {category.title()}")
                    for item in items:
                        lines.append(f"- {item}")
                    lines.append("")

        lines.extend(["## Steps", ""])
        for step in self.steps:
            lines.append(f"### Step {step.number}: {step.title}")
            lines.append(f"{step.description}")
            if step.estimated_time:
                lines.append(f"**Time**: {step.estimated_time}")
            if step.commands:
                lines.extend(["", "```bash"])
                for cmd in step.commands:
                    lines.append(cmd)
                lines.extend(["```", ""])
            if step.expected_output:
                lines.extend(["**Expected Output**:", "```", step.expected_output, "```", ""])
            if step.checklist:
                lines.append("**Checklist**:")
                for item in step.checklist:
                    lines.append(f"- [ ] {item}")
                lines.append("")
            if step.tips:
                lines.append("**Tips**:")
                for tip in step.tips:
                    lines.append(f"- 💡 {tip}")
                lines.append("")
            if step.warnings:
                lines.append("**Warnings**:")
                for warn in step.warnings:
                    lines.append(f"- ⚠️ {warn}")
                lines.append("")

        if self.troubleshooting:
            lines.extend(["## Troubleshooting", ""])
            for item in self.troubleshooting:
                lines.append(f"### {item.problem}")
                lines.append(f"**Symptom**: {item.symptom}")
                lines.append(f"**Cause**: {item.cause}")
                lines.append(f"**Solution**: {item.solution}")
                lines.append("")

        if self.expected_results:
            lines.extend(["## Expected Results", "", self.expected_results, ""])

        if self.data_collection_template:
            lines.extend(["## Data Collection Template", ""])
            dct = self.data_collection_template
            if dct.get("metrics_to_record"):
                lines.append("### Metrics to Record")
                for m in dct["metrics_to_record"]:
                    lines.append(f"- {m}")
                lines.append("")
            if dct.get("figures_to_generate"):
                lines.append("### Figures to Generate")
                for f in dct["figures_to_generate"]:
                    lines.append(f"- {f}")
                lines.append("")
            if dct.get("files_to_save"):
                lines.append("### Files to Save")
                for f in dct["files_to_save"]:
                    lines.append(f"- {f}")
                lines.append("")

        lines.append("---")
        lines.append("*Generated by Research-Nexus Pro*")
        return "\n".join(lines)


class LLMGuideGenerator:
    """LLM-driven experiment guide generator"""

    def __init__(self, llm_client: Optional[LLMClient] = None):
        self.llm = llm_client or LLMClient()

    async def generate_guide(
        self,
        innovation_data: Dict[str, Any],
        feasibility_result: Optional[Dict[str, Any]] = None,
        target_venue: str = "NeurIPS",
        experiment_type: str = "computational",
    ) -> ExperimentGuide:
        """
        Generate a detailed experiment guide for an innovation point.

        Args:
            innovation_data: The innovation point data
            feasibility_result: Optional feasibility assessment result
            target_venue: Target conference/journal
            experiment_type: Type of experiment (computational, simulation, hardware, etc.)
        """
        title = innovation_data.get("title", "Research Experiment")
        problem = innovation_data.get("problem_statement", "")
        solution = innovation_data.get("proposed_solution", "")
        implementation = innovation_data.get("implementation_path", [])

        impl_steps = "\n".join(
            f"- Step {i+1}: {step.get('description', '')}"
            for i, step in enumerate(implementation[:10])
        )

        feasibility_text = ""
        if feasibility_result:
            feasibility_text = json.dumps(feasibility_result, indent=2)

        prompt = f"""# Experiment Guide Generation Request

## Research Context

Title: {title}
Problem: {problem[:600]}
Proposed Solution: {solution[:600]}

## Implementation Plan

{impl_steps if impl_steps else "No detailed implementation plan provided."}

## Feasibility Assessment

{feasibility_text if feasibility_text else "No feasibility assessment provided."}

## Target Venue Requirements

{target_venue} — Top-tier venue requiring rigorous experimental validation.

## Experiment Type

{experiment_type}

## Instructions

Generate a complete, foolproof experiment guide that a beginner could follow.
The guide must include:
1. All software to install (with exact commands)
2. All data to download (with URLs if known)
3. Step-by-step instructions with exact commands
4. Expected output for each step
5. Troubleshooting for the 3 most common problems
6. A data collection template so the user knows exactly what to record

Make it specific to this research — generic instructions are not acceptable.
"""

        try:
            response = await self.llm.generate(
                prompt=prompt,
                system_prompt=_GUIDE_SYSTEM_PROMPT,
                temperature=0.4,
                max_tokens=4096,
            )

            parsed = self._parse_guide_json(response.text)
            if parsed:
                return parsed
            else:
                logger.warning("Could not parse guide JSON, using fallback")
                return self._fallback_guide(title, experiment_type)

        except Exception as e:
            logger.error(f"Guide generation failed: {e}")
            return self._fallback_guide(title, experiment_type)

    def _parse_guide_json(self, text: str) -> Optional[ExperimentGuide]:
        """Parse LLM response into ExperimentGuide"""
        try:
            text = text.strip()
            if text.startswith("```"):
                text = text.split("\n", 1)[1]
            if text.endswith("```"):
                text = text.rsplit("\n", 1)[0]

            data = json.loads(text.strip())

            steps = []
            for step_data in data.get("steps", []):
                steps.append(GuideStep(
                    number=step_data.get("number", 0),
                    title=step_data.get("title", ""),
                    description=step_data.get("description", ""),
                    commands=step_data.get("commands", []),
                    expected_output=step_data.get("expected_output", ""),
                    tips=step_data.get("tips", []),
                    warnings=step_data.get("warnings", []),
                    checklist=step_data.get("checklist", []),
                    estimated_time=step_data.get("estimated_time", ""),
                ))

            troubleshooting = []
            for item_data in data.get("troubleshooting", []):
                troubleshooting.append(TroubleshootingItem(
                    problem=item_data.get("problem", ""),
                    symptom=item_data.get("symptom", ""),
                    cause=item_data.get("cause", ""),
                    solution=item_data.get("solution", ""),
                ))

            return ExperimentGuide(
                title=data.get("title", "Experiment Guide"),
                description=data.get("description", ""),
                difficulty=data.get("difficulty", "intermediate"),
                estimated_time=data.get("estimated_time", ""),
                prerequisites=data.get("prerequisites", []),
                safety_notes=data.get("safety_notes", []),
                steps=steps,
                troubleshooting=troubleshooting,
                resources=data.get("resources", {}),
                expected_results=data.get("expected_results", ""),
                data_collection_template=data.get("data_collection_template", {}),
            )

        except (json.JSONDecodeError, KeyError, TypeError) as e:
            logger.warning(f"Failed to parse guide JSON: {e}")
            return None

    def _fallback_guide(self, title: str, experiment_type: str) -> ExperimentGuide:
        """Fallback guide when LLM is unavailable"""
        return ExperimentGuide(
            title=f"Experiment Guide: {title}",
            description="[LLM unavailable — this is a generic template. Set KIMI_API_KEY for customized guides.]",
            difficulty="intermediate",
            estimated_time="2-4 hours",
            prerequisites=["Python 3.9+", "pip", "Git"],
            steps=[
                GuideStep(
                    number=1,
                    title="Environment Setup",
                    description="Install required dependencies.",
                    commands=["pip install -r requirements.txt"],
                    expected_output="Successfully installed packages",
                    estimated_time="10 minutes",
                ),
                GuideStep(
                    number=2,
                    title="Data Preparation",
                    description="Download and prepare datasets.",
                    commands=["python prepare_data.py"],
                    expected_output="Dataset ready",
                    estimated_time="15 minutes",
                ),
                GuideStep(
                    number=3,
                    title="Run Experiment",
                    description="Execute the main experiment.",
                    commands=["python main.py"],
                    expected_output="Training complete, results saved",
                    estimated_time="1-2 hours",
                ),
            ],
            troubleshooting=[
                TroubleshootingItem(
                    problem="Dependency installation fails",
                    symptom="pip install throws errors",
                    cause="Incompatible Python version or missing system dependencies",
                    solution="Check Python version (must be 3.9+). Install system dependencies listed in README.",
                ),
            ],
            expected_results="[Set KIMI_API_KEY for customized expected results]",
        )
